		<h4><span class="dashicons dashicons-editor-help"></span> How to use Hover Link Plugin?</h4>
		Just add the effect class to links, buttons, logos, SVG, featured images and so on.	The class is under an effect  name	
		<h4><span class="dashicons dashicons-editor-help"></span> How to add an effects to menu?</h4>
		<ul>
			<li>Go to page Hover Effects</li>
			<li>Select effect and copy class (begin with 'hvr-')</li>			
			<li>Go to Appearance -> Menus, select which menu item to which you want to add the effect, and add the effect class under 'CSS Classes (optional)'</li>
			<li>Save your menu</li>
		</ul>
		<h4><span class="dashicons dashicons-editor-help"></span> Why don't I see an option to add classes?</h4>
		Under Appearance -> Menus, click 'Screen Options' (top right of screen) and make sure that 'CSS Classes' is checked. If not - check it!		
